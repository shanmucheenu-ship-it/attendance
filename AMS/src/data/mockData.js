  let idCounter = 1;

  // Random Indian names
  const firstNamesM = ["Arjun", "Rahul", "Vikram", "Rohan", "Aditya", "Siddharth", "Karan", "Rishabh", "Dev", "Yash", "Ravi", "Aarav", "Aryan", "Dhruv", "Rudra", "Reyansh"];
